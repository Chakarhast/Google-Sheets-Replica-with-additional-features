// All general functions applicable to every cell


// Function to expand a cell
function expandCell(cell) {
  cell.style.width = '400px'; 
  cell.style.height = '200px'; 
  
}


// Function to reset the cell
function resetCell(cell) {
  cell.style.width = ''; 
  cell.style.height = ''; 
  cell.style.backgroundColor = ''; 
  
}
